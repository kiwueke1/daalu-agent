"""Per-tenant Nautobot K8s manifests.

Pure functions: take a ``TenantParams`` dataclass + the bootstrap
credentials, return manifest dicts. No K8s API calls, no I/O — that
lives in :mod:`nautobot_controller.k8s`. Splitting them keeps the
manifest shapes easy to unit-test (the existing test pattern just
asserts on the returned dict, no httpx/kubernetes-asyncio mocking).

The manifest set mirrors the existing shared-Nautobot stack at
``deploy/k8s/nautobot/`` — same images, same env vars, same probe
shapes. The differences are all parameterisations:

* The ``namespace`` becomes ``nautobot-<slug>`` instead of
  ``nautobot``.
* The hostname in ``NAUTOBOT_ALLOWED_HOSTS`` /
  ``NAUTOBOT_CSRF_TRUSTED_ORIGINS`` becomes the per-tenant
  subdomain.
* All credentials (POSTGRES_PASSWORD, NAUTOBOT_SECRET_KEY,
  NAUTOBOT_SUPERUSER_PASSWORD, NAUTOBOT_SUPERUSER_API_TOKEN) are
  generated by the controller at provision time, stored encrypted
  in the ``nautobot_tenants`` row, and stamped into a per-namespace
  Secret. The Nautobot entrypoint's ``CREATE_SUPERUSER=true`` path
  then bootstraps the admin user with the *exact* API token we
  passed — no separate "mint a token" step.

In customer-cluster mode (Phase 2) we still generate all manifests
but skip the public Ingress (set ``include_ingress=False`` on
:func:`build_all`) — daalu-api reaches the workspace through the wg
tunnel + in-cluster Service DNS.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any


@dataclass
class TenantParams:
    """Inputs to the manifest builders.

    Carried separately from :class:`NautobotTenant` so the manifest
    code is pure and trivially testable; the controller's reconcile
    loop builds this dataclass from the DB row.
    """

    # Daalu tenant slug — used to derive the namespace, hostname,
    # and labels. Must already be DNS-1123 compliant (the daalu
    # tenant validation enforces that).
    slug: str
    # Daalu tenant display name — surfaced in Nautobot's
    # ``description`` field on the (single) Nautobot Tenant row that
    # the bootstrap Job creates.
    name: str
    # Where the stack lives. None → operator cluster (public
    # ingress + cert-manager). Anything else (the cluster tunnel
    # id) → customer cluster (no ingress).
    target_cluster_tunnel_id: str | None = None
    # ``<slug>.sot.example.com`` for operator-cluster; None for
    # customer-cluster (daalu reaches via Service DNS + wg).
    hostname: str | None = None

    # Bootstrap credentials — generated by the controller at
    # provision time, then reused on every reconcile so the
    # Postgres/Nautobot pair don't lose their auth when manifests
    # are re-applied.
    postgres_password: str = ""
    nautobot_secret_key: str = ""
    admin_password: str = ""
    admin_token: str = ""

    # Image pin. The shared-Nautobot deploy lives at
    # ``networktocode/nautobot:2.2-py3.11``; per-tenant follows the
    # same default so a single CVE bump rolls every tenant.
    nautobot_image: str = "networktocode/nautobot:2.2-py3.11"
    postgres_image: str = "postgres:16-alpine"
    redis_image: str = "redis:7-alpine"

    # Storage for the per-tenant Postgres PVC. Small default;
    # the wizard can offer larger tiers later.
    postgres_storage: str = "20Gi"
    # Storage for the Nautobot media volume (uploads, custom
    # field attachments). Small — most tenants never hit a GB.
    nautobot_storage: str = "5Gi"

    # Operator-side resource caps. Conservative; the
    # single-node box can hold ~30 of these.
    nautobot_cpu_request: str = "100m"
    nautobot_cpu_limit: str = "1"
    nautobot_mem_request: str = "256Mi"
    nautobot_mem_limit: str = "1Gi"

    # Optional extra labels — every manifest gets these so a
    # ``kubectl get all -l daalu.io/nautobot-tenant=<slug>`` works.
    extra_labels: dict[str, str] = field(default_factory=dict)


# ── Namespace + Secret ─────────────────────────────────────────────────


def namespace_for_slug(slug: str) -> str:
    """The deterministic namespace name for a tenant.

    Kept as a free function so callers (controller reconcile loop,
    teardown helpers, the engineer doc's verification commands) all
    derive the same name from one place.
    """
    return f"nautobot-{slug}"


def build_namespace(p: TenantParams) -> dict[str, Any]:
    return {
        "apiVersion": "v1",
        "kind": "Namespace",
        "metadata": {
            "name": namespace_for_slug(p.slug),
            "labels": _labels(p, role="namespace"),
        },
    }


def build_secret(p: TenantParams) -> dict[str, Any]:
    """The per-namespace credentials Secret.

    Holds every value the per-tenant Nautobot pod + Postgres need.
    The controller writes this once at provision time and re-asserts
    it on every reconcile — rewriting with identical values is a
    no-op against the K8s control plane.

    NB: ``stringData`` (not ``data``) so we don't have to base64 the
    values ourselves; K8s does it. SOPS / encryption-at-rest live at
    the daalu DB layer (the ciphertext columns on nautobot_tenants),
    not on the K8s Secret itself.
    """
    return {
        "apiVersion": "v1",
        "kind": "Secret",
        "type": "Opaque",
        "metadata": {
            "name": "nautobot-secrets",
            "namespace": namespace_for_slug(p.slug),
            "labels": _labels(p, role="secret"),
        },
        "stringData": {
            "POSTGRES_PASSWORD": p.postgres_password,
            "NAUTOBOT_DB_PASSWORD": p.postgres_password,
            "NAUTOBOT_SECRET_KEY": p.nautobot_secret_key,
            "NAUTOBOT_SUPERUSER_PASSWORD": p.admin_password,
            # The Nautobot entrypoint accepts an explicit API token to
            # use for the bootstrapped superuser. We pass our
            # pre-generated value so the controller knows exactly
            # which token to hand back to daalu-api on completion —
            # no second round-trip to mint one.
            "NAUTOBOT_SUPERUSER_API_TOKEN": p.admin_token,
            # Email is required by the bootstrap but never used.
            "NAUTOBOT_SUPERUSER_EMAIL": "admin@daalu.io",
        },
    }


# ── ConfigMap (env) ────────────────────────────────────────────────────


def build_configmap(p: TenantParams) -> dict[str, Any]:
    """Non-secret env. Mirrors deploy/k8s/nautobot/configmap.yaml.

    The hostname-derived fields change per tenant; everything else
    is constant. ALLOWED_HOSTS uses *space* separation (not comma) —
    Nautobot's settings parser splits on whitespace.
    """
    ns = namespace_for_slug(p.slug)
    public_host = p.hostname or ""
    allowed = ["nautobot", f"nautobot.{ns}.svc.cluster.local", "localhost", "127.0.0.1"]
    csrf: list[str] = []
    if public_host:
        allowed.insert(0, public_host)
        csrf.append(f"https://{public_host}")
    return {
        "apiVersion": "v1",
        "kind": "ConfigMap",
        "metadata": {
            "name": "nautobot-env",
            "namespace": ns,
            "labels": _labels(p, role="configmap"),
        },
        "data": {
            "NAUTOBOT_DB_HOST": f"nautobot-postgres.{ns}.svc.cluster.local",
            "NAUTOBOT_DB_PORT": "5432",
            "NAUTOBOT_DB_NAME": "nautobot",
            "NAUTOBOT_DB_USER": "nautobot",
            "NAUTOBOT_REDIS_HOST": f"nautobot-redis.{ns}.svc.cluster.local",
            "NAUTOBOT_REDIS_PORT": "6379",
            "NAUTOBOT_ALLOWED_HOSTS": " ".join(allowed),
            "NAUTOBOT_CSRF_TRUSTED_ORIGINS": " ".join(csrf),
            "NAUTOBOT_CREATE_SUPERUSER": "true",
            "NAUTOBOT_SUPERUSER_NAME": "admin",
        },
    }


# ── Postgres ────────────────────────────────────────────────────────────


def build_postgres(p: TenantParams) -> list[dict[str, Any]]:
    """Postgres PVC + StatefulSet + headless Service.

    One Postgres per tenant — operationally heavier than a shared
    cluster with per-tenant databases, but it preserves the strongest
    isolation guarantee (a bad Postgres role-grant can't cross
    tenants). If the per-tenant pod count starts to bite, swap in
    CloudNativePG without changing the rest of this module — the
    Service name (``nautobot-postgres``) is what Nautobot reads.
    """
    ns = namespace_for_slug(p.slug)
    pvc = {
        "apiVersion": "v1",
        "kind": "PersistentVolumeClaim",
        "metadata": {
            "name": "nautobot-postgres-data",
            "namespace": ns,
            "labels": _labels(p, role="postgres"),
        },
        "spec": {
            "accessModes": ["ReadWriteOnce"],
            "resources": {"requests": {"storage": p.postgres_storage}},
        },
    }
    sts = {
        "apiVersion": "apps/v1",
        "kind": "StatefulSet",
        "metadata": {
            "name": "nautobot-postgres",
            "namespace": ns,
            "labels": _labels(p, role="postgres"),
        },
        "spec": {
            "serviceName": "nautobot-postgres",
            "replicas": 1,
            "selector": {"matchLabels": {"app": "nautobot-postgres"}},
            "template": {
                "metadata": {
                    "labels": {
                        "app": "nautobot-postgres",
                        **_labels(p, role="postgres"),
                    }
                },
                "spec": {
                    "automountServiceAccountToken": False,
                    "securityContext": {
                        "runAsNonRoot": True,
                        "runAsUser": 999,
                        "runAsGroup": 999,
                        "fsGroup": 999,
                    },
                    "containers": [
                        {
                            "name": "postgres",
                            "image": p.postgres_image,
                            "env": [
                                {"name": "POSTGRES_DB", "value": "nautobot"},
                                {"name": "POSTGRES_USER", "value": "nautobot"},
                                {
                                    "name": "POSTGRES_PASSWORD",
                                    "valueFrom": {
                                        "secretKeyRef": {
                                            "name": "nautobot-secrets",
                                            "key": "POSTGRES_PASSWORD",
                                        }
                                    },
                                },
                                {
                                    "name": "PGDATA",
                                    "value": "/var/lib/postgresql/data/pgdata",
                                },
                            ],
                            "ports": [{"containerPort": 5432}],
                            "readinessProbe": {
                                "exec": {
                                    "command": ["pg_isready", "-U", "nautobot"],
                                },
                                "initialDelaySeconds": 10,
                                "periodSeconds": 5,
                            },
                            "volumeMounts": [
                                {
                                    "name": "data",
                                    "mountPath": "/var/lib/postgresql/data",
                                }
                            ],
                            "resources": {
                                "requests": {"cpu": "50m", "memory": "256Mi"},
                                "limits": {"cpu": "1", "memory": "1Gi"},
                            },
                        }
                    ],
                    "volumes": [
                        {
                            "name": "data",
                            "persistentVolumeClaim": {
                                "claimName": "nautobot-postgres-data"
                            },
                        }
                    ],
                },
            },
        },
    }
    svc = {
        "apiVersion": "v1",
        "kind": "Service",
        "metadata": {
            "name": "nautobot-postgres",
            "namespace": ns,
            "labels": _labels(p, role="postgres"),
        },
        "spec": {
            "selector": {"app": "nautobot-postgres"},
            "ports": [{"port": 5432, "targetPort": 5432}],
            # Headless so the StatefulSet pod gets a stable DNS name
            # (nautobot-postgres-0.nautobot-postgres). Nautobot only
            # uses the Service DNS, but a future read-replica setup
            # would want pod-level addressing.
            "clusterIP": "None",
        },
    }
    return [pvc, sts, svc]


