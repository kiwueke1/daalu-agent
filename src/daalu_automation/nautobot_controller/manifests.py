# ── Ingress (operator-cluster only) ────────────────────────────────────


def build_ingress(p: TenantParams) -> dict[str, Any]:
    """Public traefik Ingress + cert-manager HTTP-01 TLS.

    Only used in operator-cluster mode. Customer-cluster mode skips
    this entirely — daalu-api reaches the pod via the wg tunnel and
    in-cluster Service DNS, no public hostname required.
    """
    if not p.hostname:
        raise ValueError("build_ingress requires hostname (operator-cluster mode)")
    ns = namespace_for_slug(p.slug)
    return {
        "apiVersion": "networking.k8s.io/v1",
        "kind": "Ingress",
        "metadata": {
            "name": "nautobot",
            "namespace": ns,
            "labels": _labels(p, role="ingress"),
            "annotations": {
                "cert-manager.io/cluster-issuer": "letsencrypt-prod",
            },
        },
        "spec": {
            "ingressClassName": "traefik",
            "tls": [
                {
                    "hosts": [p.hostname],
                    "secretName": f"{p.slug}-sot-daalu-io-tls",
                }
            ],
            "rules": [
                {
                    "host": p.hostname,
                    "http": {
                        "paths": [
                            {
                                "path": "/",
                                "pathType": "Prefix",
                                "backend": {
                                    "service": {
                                        "name": "nautobot",
                                        "port": {"number": 80},
                                    }
                                },
                            }
                        ]
                    },
                }
            ],
        },
    }


# ── NetworkPolicy ──────────────────────────────────────────────────────


def build_networkpolicy(p: TenantParams) -> list[dict[str, Any]]:
    """Default-deny posture + the narrow allows we actually need.

    Postgres/Redis are intra-namespace (the Nautobot pods are the only
    callers); web pod takes ingress from traefik (operator-cluster) or
    daalu-api over the wg tunnel (customer-cluster — the policy allows
    the relevant CIDR via the kube-system DNS allowlist below).
    """
    ns = namespace_for_slug(p.slug)
    out: list[dict[str, Any]] = [
        {
            "apiVersion": "networking.k8s.io/v1",
            "kind": "NetworkPolicy",
            "metadata": {"name": "default-deny", "namespace": ns},
            "spec": {"podSelector": {}, "policyTypes": ["Ingress", "Egress"]},
        },
        # Intra-namespace pod-to-pod (web → postgres, web → redis,
        # worker → postgres, worker → redis).
        {
            "apiVersion": "networking.k8s.io/v1",
            "kind": "NetworkPolicy",
            "metadata": {"name": "allow-intra-ns", "namespace": ns},
            "spec": {
                "podSelector": {},
                "policyTypes": ["Ingress", "Egress"],
                "ingress": [{"from": [{"podSelector": {}}]}],
                "egress": [{"to": [{"podSelector": {}}]}],
            },
        },
        # DNS + general HTTPS egress so Nautobot can fetch updates,
        # contact a Git source for Apps, etc.
        {
            "apiVersion": "networking.k8s.io/v1",
            "kind": "NetworkPolicy",
            "metadata": {"name": "allow-egress-dns-https", "namespace": ns},
            "spec": {
                "podSelector": {"matchLabels": {"app": "nautobot"}},
                "policyTypes": ["Egress"],
                "egress": [
                    {
                        "to": [
                            {
                                "namespaceSelector": {
                                    "matchLabels": {
                                        "kubernetes.io/metadata.name": "kube-system"
                                    }
                                }
                            }
                        ],
                        "ports": [
                            {"protocol": "UDP", "port": 53},
                            {"protocol": "TCP", "port": 53},
                        ],
                    },
                    {
                        "to": [{"ipBlock": {"cidr": "0.0.0.0/0"}}],
                        "ports": [{"protocol": "TCP", "port": 443}],
                    },
                ],
            },
        },
    ]
    if p.hostname:
        out.append(
            {
                "apiVersion": "networking.k8s.io/v1",
                "kind": "NetworkPolicy",
                "metadata": {"name": "allow-traefik-to-web", "namespace": ns},
                "spec": {
                    "podSelector": {"matchLabels": {"app": "nautobot"}},
                    "policyTypes": ["Ingress"],
                    "ingress": [
                        {
                            "from": [
                                {
                                    "namespaceSelector": {
                                        "matchLabels": {
                                            "kubernetes.io/metadata.name": "kube-system"
                                        }
                                    }
                                }
                            ],
                            "ports": [{"protocol": "TCP", "port": 8080}],
                        }
                    ],
                },
            }
        )
    else:
        # Customer-cluster mode: daalu-api reaches the web pod through
        # the wg tunnel. The exact source is the wireguard interface
        # address on the customer cluster; lock to a broad allow on
        # port 8080 and rely on the tunnel for the perimeter.
        out.append(
            {
                "apiVersion": "networking.k8s.io/v1",
                "kind": "NetworkPolicy",
                "metadata": {"name": "allow-cluster-to-web", "namespace": ns},
                "spec": {
                    "podSelector": {"matchLabels": {"app": "nautobot"}},
                    "policyTypes": ["Ingress"],
                    "ingress": [
                        {
                            "from": [{"ipBlock": {"cidr": "0.0.0.0/0"}}],
                            "ports": [{"protocol": "TCP", "port": 8080}],
                        }
                    ],
                },
            }
        )
    return out


# ── Bundle helper ──────────────────────────────────────────────────────


def build_all(
    p: TenantParams, *, include_ingress: bool | None = None
) -> list[dict[str, Any]]:
    """Return the full stack of manifests in apply order.

    Order matters when applying for the first time: namespace →
    Secret/ConfigMap → Postgres/Redis (Nautobot's initContainer
    waits on Postgres readiness) → Nautobot pods → Ingress.
    Re-applies in any order are safe because each manifest is named
    independently and server-side apply tolerates re-asserts.

    ``include_ingress`` defaults to True when ``p.hostname`` is set
    (operator-cluster mode) and False otherwise.
    """
    if include_ingress is None:
        include_ingress = bool(p.hostname)
    out: list[dict[str, Any]] = [
        build_namespace(p),
        build_secret(p),
        build_configmap(p),
        *build_postgres(p),
        *build_redis(p),
        *build_nautobot_web(p),
        build_nautobot_worker(p),
        build_nautobot_scheduler(p),
        *build_networkpolicy(p),
    ]
    if include_ingress:
        out.append(build_ingress(p))
    return out


