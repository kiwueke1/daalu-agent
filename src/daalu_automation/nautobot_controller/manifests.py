# ── Nautobot web / worker / scheduler ──────────────────────────────────


def _nautobot_envfrom() -> list[dict[str, Any]]:
    return [
        {"configMapRef": {"name": "nautobot-env"}},
        {"secretRef": {"name": "nautobot-secrets"}},
    ]


def _nautobot_pod_security() -> dict[str, Any]:
    # Mirrors deploy/k8s/nautobot/web/deployment.yaml — the upstream
    # image runs as uid/gid 999.
    return {
        "runAsNonRoot": True,
        "runAsUser": 999,
        "runAsGroup": 999,
        "fsGroup": 999,
    }


def build_nautobot_web(p: TenantParams) -> list[dict[str, Any]]:
    ns = namespace_for_slug(p.slug)
    host = p.hostname or f"nautobot.{ns}.svc.cluster.local"
    probe = {
        "httpGet": {
            "path": "/health/",
            "port": 8080,
            "httpHeaders": [{"name": "Host", "value": host}],
        },
        "initialDelaySeconds": 30,
        "periodSeconds": 10,
        "timeoutSeconds": 5,
    }
    dep = {
        "apiVersion": "apps/v1",
        "kind": "Deployment",
        "metadata": {
            "name": "nautobot",
            "namespace": ns,
            "labels": _labels(p, role="nautobot-web"),
        },
        "spec": {
            "replicas": 1,
            "selector": {"matchLabels": {"app": "nautobot"}},
            "template": {
                "metadata": {
                    "labels": {"app": "nautobot", **_labels(p, role="nautobot-web")}
                },
                "spec": {
                    "automountServiceAccountToken": False,
                    "securityContext": _nautobot_pod_security(),
                    "initContainers": [
                        {
                            "name": "migrate",
                            "image": p.nautobot_image,
                            "command": ["nautobot-server", "migrate"],
                            "envFrom": _nautobot_envfrom(),
                            "resources": {
                                "requests": {"cpu": "100m", "memory": "256Mi"},
                                "limits": {"cpu": "1", "memory": "1Gi"},
                            },
                        }
                    ],
                    "containers": [
                        {
                            "name": "nautobot",
                            "image": p.nautobot_image,
                            "ports": [{"containerPort": 8080}],
                            "envFrom": _nautobot_envfrom(),
                            "readinessProbe": probe,
                            "livenessProbe": {
                                **probe,
                                "initialDelaySeconds": 90,
                                "periodSeconds": 30,
                                "timeoutSeconds": 10,
                            },
                            "resources": {
                                "requests": {
                                    "cpu": p.nautobot_cpu_request,
                                    "memory": p.nautobot_mem_request,
                                },
                                "limits": {
                                    "cpu": p.nautobot_cpu_limit,
                                    "memory": p.nautobot_mem_limit,
                                },
                            },
                        }
                    ],
                },
            },
        },
    }
    svc = {
        "apiVersion": "v1",
        "kind": "Service",
        "metadata": {
            "name": "nautobot",
            "namespace": ns,
            "labels": _labels(p, role="nautobot-web"),
        },
        "spec": {
            "selector": {"app": "nautobot"},
            "ports": [{"port": 80, "targetPort": 8080}],
        },
    }
    return [dep, svc]


