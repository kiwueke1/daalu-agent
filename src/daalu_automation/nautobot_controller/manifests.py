def _labels(p: TenantParams, *, role: str) -> dict[str, str]:
    base = {
        "daalu.io/nautobot-tenant": p.slug,
        "daalu.io/role": role,
        "app.kubernetes.io/part-of": "daalu-nautobot",
    }
    base.update(p.extra_labels)
    return base
